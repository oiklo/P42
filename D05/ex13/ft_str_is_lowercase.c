int		ft_str_is_lowercase(char *str)
{
	int a;

	a = 0;
	if (str[0] != '\0')
	{
		while (str[a] != '\0')
		{
			while ((str[a] >= 'a' && str[a] <= 'z'))
			{
				a++;
			}
			if (str[a] == '\0')
				return (1);
			else
				return (0);
		}
	}
	return (1);
}

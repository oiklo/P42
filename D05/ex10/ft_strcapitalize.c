char	*ft_strcapitalize(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[0] >= 'a' && str[0] <= 'z')
			str[a] -= 32;
		while (str[a] == 32 || str[a] == '+' || str[a] == '-')
		{
			a++;
			if (str[a] >= 'a' && str[a] <= 'z')
				str[a] -= 32;
		}
		a++;
		if (str[a] >= 'A' && str[a] <= 'Z')
			str[a] += 32;
	}
	return (str);
}

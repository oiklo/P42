#include "libft.h"

char	*checknb1(char *nb1)
{
	int i;

	i = 0;
	while (nb1[i])
	{
		if (nb1[i] == '+' || nb1[i] == '-')
			i++;
		if (nb1[i] >= 48 && nb1[i] <= 57)
		{
			while (nb1[i] >= 48 && nb1[i] <= 57)
				i++;
		}
		else
		{
			nb1[i] = 0;
			i++;
		}
		while (nb1[i])
		{
			nb1[i] = '\0';
			i++;
		}
	}
	return (nb1);
}

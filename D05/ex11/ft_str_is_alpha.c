int		ft_str_is_alpha(char *str)
{
	int a;

	a = 0;
	if (str[0] == '\0')
		return (1);
	while ((str[a] >= 'a' && str[a] <= 'z') ||
			(str[a] >= 'A' && str[a] <= 'Z'))
	{
		a++;
		if (str[a] == '\0')
			return (1);
	}
	return (0);
}

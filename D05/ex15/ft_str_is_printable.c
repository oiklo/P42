int		ft_str_is_printable(char *str)
{
	int a;

	a = 0;
	if (str[0] != '\0')
	{
		while (str[a] != '\0')
		{
			while ((str[a] >= 32 && str[a] < 127))
			{
				a++;
			}
			if (str[a] == '\0')
				return (1);
			else
				return (0);
		}
	}
	return (1);
}
